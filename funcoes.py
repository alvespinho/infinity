def somar (num1: float, num2: float):
    result = num1 + num2
    return result

def subtrair (num1: float, num2: float):
    result = num1 - num2
    return result

def multiplicar (num1: float, num2: float):
    result = num1 * num2
    return result

def dividir (num1: float, num2: float):
    result = num1 / num2
    return result

def potencia (num1: float, num2: float):
    result = num1 ** num2
    return result




